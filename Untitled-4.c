/* This program inputs two integer values,two character values and two float values and displays them back on the screen*/

# include <stdio.h>

int main(){
    int a,b;
    scanf("%d %d",&a,&b);
    printf("Integer1=%d \n",a);
    printf("Integer2=%d\n",b);
    float c,d;
    scanf("%f %f",&c,&d);
    printf("Float1=%f\n",c);
    printf("Float2=%f\n",d);
    char x,y;
    scanf("%c %c",&x,&y);
    printf("Character1=%c\n",x);
    printf("Character2=%c\n",y);
 }  /* End of the program */ 
